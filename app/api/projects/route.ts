import { NextRequest, NextResponse } from "next/server";
import { PrismaClient } from '@prisma/client';
import { getServerSession } from 'next-auth';
import { authOptions } from '@/lib/auth';

const prisma = new PrismaClient();

// GET /api/projects - R√©cup√©rer tous les projets
export async function GET(request: NextRequest) {
  try {
    // V√©rifier l'authentification
    const session = await getServerSession(authOptions);
    if (!session?.user) {
      return NextResponse.json({ error: "Non authentifi√©" }, { status: 401 });
    }

    const { searchParams } = new URL(request.url);
    const concepteurId = searchParams.get('concepteurId');
    const includeWorks = searchParams.get('includeWorks') === 'true';
    const status = searchParams.get('status');
    
    // Construire l'include de base
    const baseInclude: any = {
      concepteur: {
        select: {
          id: true,
          name: true,
          email: true,
          role: true
        }
      },
      discipline: {
        select: {
          id: true,
          name: true
        }
      },
      reviewer: {
        select: {
          id: true,
          name: true,
          email: true
        }
      }
    };

    // Ajouter works si demand√© et disponible
    if (includeWorks) {
      try {
        // Test si la relation works existe
        await prisma.project.findFirst({
          include: { works: true }
        });
        baseInclude.works = {
          include: {
            orderItems: true,
            royalties: true
          },
          orderBy: {
            createdAt: 'desc'
          }
        };
      } catch (worksError) {
        console.log("‚ö†Ô∏è Relation works non disponible, continuation sans works");
      }
    }
    
    // Construire la clause where
    let whereClause: any = {};
    if (concepteurId) {
      whereClause.concepteurId = concepteurId;
    }
    
    // Filtrer par statut si demand√©
    if (status) {
      whereClause.status = status;
    }

    // Log pour debug
    console.log("üîç API Projects - Utilisateur:", session.user.email, "R√¥le:", session.user.role);
    console.log("üîç API Projects - Param√®tres:", { concepteurId, status, includeWorks });
    console.log("üîç API Projects - Clause where:", whereClause);

    const projects = await prisma.project.findMany({
      where: whereClause,
      include: baseInclude,
      orderBy: {
        createdAt: 'desc'
      }
    });

    console.log("üîç API Projects - R√©sultat:", projects.length, "projets trouv√©s");
    if (projects.length > 0) {
      projects.forEach((project, index) => {
        console.log(`   ${index + 1}. "${project.title}" (${project.status}) - ${project.concepteur?.name}`);
      });
    }

    return NextResponse.json(projects, { status: 200 });
  } catch (error: any) {
    console.error("Error fetching projects:", error);
    return NextResponse.json(
      { error: "Erreur lors de la r√©cup√©ration des projets: " + error.message },
      { status: 500 }
    );
  }
}

// POST /api/projects - Cr√©er un nouveau projet
export async function POST(request: NextRequest) {
  console.log("üîç API POST /projects - D√©but de la requ√™te");
  
  try {
    const body = await request.json();
    console.log("üîç Body re√ßu:", body);
    
    const { 
      title, 
      disciplineId, 
      concepteurId, 
      description,
      status = "DRAFT" 
    } = body;
    
    console.log("üîç Donn√©es extraites:", { title, disciplineId, concepteurId, description, status });

    // Validation des champs obligatoires
    if (!title || !disciplineId || !concepteurId) {
      return NextResponse.json(
        { error: "Le titre, la discipline et le concepteur sont obligatoires" },
        { status: 400 }
      );
    }

    // Trouver la discipline par nom ou ID
    let discipline;
    if (disciplineId.match(/^[a-zA-Z0-9_-]+$/)) {
      // Si c'est un ID (format alphanum√©rique)
      discipline = await prisma.discipline.findUnique({
        where: { id: disciplineId }
      });
    } else {
      // Si c'est un nom de discipline
      discipline = await prisma.discipline.findFirst({
        where: { name: disciplineId }
      });
    }

    if (!discipline) {
      return NextResponse.json(
        { error: "Discipline non trouv√©e" },
        { status: 400 }
      );
    }

    // Validation du statut
    const validStatuses = ["DRAFT", "SUBMITTED", "UNDER_REVIEW", "ACCEPTED", "REJECTED"];
    if (!validStatuses.includes(status)) {
      return NextResponse.json(
        { error: "Statut invalide" },
        { status: 400 }
      );
    }

    console.log("üîç Tentative de cr√©ation avec Prisma...");
    
    // Cr√©er le projet dans le mod√®le Project
    const project = await prisma.project.create({
      data: {
        title: title.trim(),
        description: description?.trim() || "",
        discipline: {
          connect: { id: discipline.id }
        },
        concepteur: {
          connect: { id: concepteurId }
        },
        status: status
      },
      include: {
        concepteur: {
          select: {
            id: true,
            name: true,
            email: true,
            role: true
          }
        },
        discipline: {
          select: {
            id: true,
            name: true
          }
        }
      }
    });

    console.log("‚úÖ Projet cr√©√©, ajout des logs et notifications...");

    // Si le statut est SUBMITTED, cr√©er automatiquement une ≈ìuvre en attente de validation
    if (status === "SUBMITTED") {
      try {
        // G√©n√©rer un ISBN unique pour l'≈ìuvre
        const isbn = `978-${Date.now().toString().slice(-9)}-${Math.floor(Math.random() * 1000).toString().padStart(3, '0')}`;
        
        const work = await prisma.work.create({
          data: {
            title: project.title,
            isbn: isbn,
            price: 0, // Prix par d√©faut, √† d√©finir lors de la validation
            stock: 0, // Stock par d√©faut
            minStock: 10,
            maxStock: null,
            status: "PENDING", // En attente de validation par le PDG
            discipline: {
              connect: { id: discipline.id }
            },
            concepteur: {
              connect: { id: concepteurId }
            },
            projectId: project.id
          },
          include: {
            concepteur: {
              select: {
                id: true,
                name: true,
                email: true,
                role: true
              }
            },
            discipline: {
              select: {
                id: true,
                name: true
              }
            }
          }
        });

        console.log("‚úÖ ≈íuvre cr√©√©e automatiquement:", work);

        // Cr√©er une notification pour le PDG
        try {
          const pdgUser = await prisma.user.findFirst({
            where: { role: "PDG" }
          });

          if (pdgUser) {
            await prisma.notification.create({
              data: {
                userId: pdgUser.id,
                title: "Nouvelle ≈ìuvre soumise",
                message: `Le concepteur ${project.concepteur.name} a soumis une nouvelle ≈ìuvre "${work.title}" pour validation.`,
                type: "WORK_SUBMITTED_FOR_VALIDATION",
                data: JSON.stringify({
                  workId: work.id,
                  workTitle: work.title,
                  concepteurId: concepteurId,
                  concepteurName: project.concepteur.name,
                  discipline: work.discipline.name,
                  isbn: work.isbn
                })
              }
            });
            console.log("‚úÖ Notification cr√©√©e pour le PDG");
          }
        } catch (notificationError) {
          console.error("‚ö†Ô∏è Erreur cr√©ation notification PDG:", notificationError);
        }

        // Cr√©er une notification pour le concepteur
        try {
          await prisma.notification.create({
            data: {
              userId: concepteurId,
              title: "Projet soumis avec succ√®s",
              message: `Votre projet "${project.title}" a √©t√© soumis pour validation et sera examin√© par l'√©quipe √©ditoriale.`,
              type: "PROJECT_SUBMITTED",
              data: JSON.stringify({
                projectId: project.id,
                workId: work.id,
                projectTitle: project.title,
                status: "SUBMITTED"
              })
            }
          });
          console.log("‚úÖ Notification cr√©√©e pour le concepteur");
        } catch (notificationError) {
          console.error("‚ö†Ô∏è Erreur cr√©ation notification concepteur:", notificationError);
        }

      } catch (workError) {
        console.error("‚ö†Ô∏è Erreur cr√©ation ≈ìuvre automatique:", workError);
        // Ne pas faire √©chouer la cr√©ation du projet pour une erreur d'≈ìuvre
      }
    }

    // Cr√©er un log d'audit
    try {
      await prisma.auditLog.create({
        data: {
          action: "PROJECT_CREATE",
          userId: concepteurId,
          performedBy: concepteurId,
          details: JSON.stringify({
            projectId: project.id,
            projectTitle: project.title,
            status: status,
            discipline: project.discipline.name
          })
        }
      });
      console.log("‚úÖ Log d'audit cr√©√©");
    } catch (auditError) {
      console.error("‚ö†Ô∏è Erreur cr√©ation log d'audit:", auditError);
    }

    // Cr√©er une notification pour le concepteur
    try {
      let notificationTitle, notificationMessage, notificationType;
      
      if (status === "DRAFT") {
        notificationTitle = "Projet cr√©√© en brouillon";
        notificationMessage = `Votre projet "${project.title}" a √©t√© sauvegard√© en brouillon.`;
        notificationType = "PROJECT_DRAFT_CREATED";
      } else if (status === "SUBMITTED") {
        notificationTitle = "Projet soumis pour validation";
        notificationMessage = `Votre projet "${project.title}" a √©t√© soumis pour validation et sera examin√© par l'√©quipe √©ditoriale.`;
        notificationType = "PROJECT_SUBMITTED";
      } else {
        notificationTitle = "Projet cr√©√©";
        notificationMessage = `Votre projet "${project.title}" a √©t√© cr√©√© avec succ√®s.`;
        notificationType = "PROJECT_CREATED";
      }

      await prisma.notification.create({
        data: {
          userId: concepteurId,
          title: notificationTitle,
          message: notificationMessage,
          type: notificationType,
          data: JSON.stringify({
            projectId: project.id,
            projectTitle: project.title,
            status: status
          })
        }
      });
      console.log("‚úÖ Notification cr√©√©e");
    } catch (notificationError) {
      console.error("‚ö†Ô∏è Erreur cr√©ation notification:", notificationError);
    }

    console.log("‚úÖ Projet cr√©√© avec succ√®s:", project);
    
    return NextResponse.json(project, { status: 201 });
    
  } catch (error: any) {
    console.error("‚ùå Erreur cr√©ation projet:", error);
    console.error("‚ùå Stack:", error.stack);
    
    // Gestion sp√©cifique des erreurs Prisma
    if (error.code === 'P2002') {
      return NextResponse.json(
        { error: "Un projet avec cet ISBN existe d√©j√†" },
        { status: 400 }
      );
    }
    
    return NextResponse.json(
      { error: "Erreur lors de la cr√©ation du projet: " + error.message },
      { status: 500 }
    );
  }
}

